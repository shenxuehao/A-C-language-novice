#define _CRT_SECURE_NO_DEPRECATE
#include <stdio.h>
int main()
{
	int a = 0;
	printf("%d\n", ~a);//~按二进制位取反，0000000000000000000000000000000
	                                    //1111111111111111111111111111111  补码，打印出的是源码
	                                    //1111111111111111111111111111110
	                                    //1000000000000000000000000000001
	int b = -1;
	printf("%d\n", ~b);                 //1000000000000000000000000000001
	                                    //0111111111111111111111111111110 补码
	                                    //0111111111111111111111111111101
	                                    //0000000000000000000000000000010
	int c = 1;
	printf("%d\n", ~c);                 //0000000000000000000000000000001
	                                    //1111111111111111111111111111110
	                                    //1111111111111111111111111111101
	                                    //1000000000000000000000000000010
	return 0;
}




